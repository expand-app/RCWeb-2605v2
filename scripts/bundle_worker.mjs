#!/usr/bin/env node
// Bundle worker/src/*.js into a single paste-able module for the Cloudflare
// dashboard editor (which wants one file). Strips local `import` lines and
// `export ` keywords, concatenates in dependency order, keeps the final
// `export default { fetch }`.
//
// Usage: node scripts/bundle_worker.mjs  → writes worker/dist/worker.bundled.js

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const SRC = join(here, "..", "worker", "src");
const OUT_DIR = join(here, "..", "worker", "dist");

// Dependency order: leaf modules first, index.js last.
const ORDER = ["auth.js", "github.js", "sync-html.js", "oss.js", "puebulo.js", "index.js"];

function strip(code) {
  return code
    // drop local relative imports: import {...} from "./x.js";
    .replace(/^\s*import\s+[^;]*?from\s+["']\.\/[^"']+["'];?\s*$/gm, "")
    // turn `export function` / `export async function` / `export const` into plain
    .replace(/^export\s+(async\s+function|function|const|let|class)/gm, "$1");
}

const header = `// ============================================================
// rexpand-admin-api — bundled single-file Worker
// Generated by scripts/bundle_worker.mjs from worker/src/*.js
// Do NOT edit by hand; edit the sources and re-run the bundler.
// ============================================================
`;

let body = "";
for (const f of ORDER) {
  const code = strip(readFileSync(join(SRC, f), "utf-8"));
  body += `\n// ----- ${f} -----\n` + code.trim() + "\n";
}

mkdirSync(OUT_DIR, { recursive: true });
const out = join(OUT_DIR, "worker.bundled.js");
writeFileSync(out, header + body);
console.log("wrote", out, "(" + (header + body).length + " bytes)");
